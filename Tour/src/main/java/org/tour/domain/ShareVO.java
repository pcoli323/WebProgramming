package org.tour.domain;

public class ShareVO {
	
	private int originalCourseNumber;
	private int copiedCourseNumber;
	
	public int getOriginalCourseNumber() {
		return originalCourseNumber;
	}
	public void setOriginalCourseNumber(int originalCourseNumber) {
		this.originalCourseNumber = originalCourseNumber;
	}
	public int getCopiedCourseNumber() {
		return copiedCourseNumber;
	}
	public void setCopiedCourseNumber(int copiedCourseNumber) {
		this.copiedCourseNumber = copiedCourseNumber;
	}

}
