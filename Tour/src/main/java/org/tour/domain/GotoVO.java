package org.tour.domain;

public class GotoVO {

	private String gotoCode;
	private String gotoName;
	
	public String getGotoCode() {
		return gotoCode;
	}
	public void setGotoCode(String gotoCode) {
		this.gotoCode = gotoCode;
	}
	public String getGotoName() {
		return gotoName;
	}
	public void setGotoName(String gotoName) {
		this.gotoName = gotoName;
	}
	
}
